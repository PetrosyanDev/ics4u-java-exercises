public class Exercise05 {
    public static void main(String[] args) {

        System.out.println("The^answer^is:^^"); // The^answer^is:^^
        System.out.print("abc"); // abc
        System.out.print("\the^answer\nis:"); // abc he^answer
        System.out.println("yes\n\nmame"); // is:yes
        System.out.println("\\\\\\n2+3"); // \\\n2+3
        System.out.print("big"); // bigNew
        System.out.println("New\nYork\n"); // York
        System.out.println("apple"); // apple
        System.out.println(34.789); // 34.789
        System.out.println(4 * 3); // 12
        System.out.println("4*3"); // 4*3
        System.out.println(5 + 2); // 7
        System.out.println("5+2"); // 5+2
        System.out.println("5" + "2"); // "52"
        System.out.println("" + 5 + 2); // "52"
        System.out.println(5 + 2 + ""); // "7"
        System.out.println(5 + "85" + 24.2 + "\n5*2"); // 58524.2
        System.out.println("8+8=^" + 8 + 8); // 8+8=^88
        System.out.println(8 + 8 + "=8*8"); // 16=8*8
    }
}
